#include "../libftprintf.h"

int	ft_putnb(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return (11);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr((n / 10));
		n = n % 10;
	}
	if (n < 10)
	{
		ft_putchar((n + 48));
	}
}
